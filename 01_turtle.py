import turtle as t
t.shape('turtle')

# 삼각형 그리기
t.color("red")
t.forward(300)
t.left(120)
t.forward(300)
t.left(120)
t.forward(300)
t.left(120)

# 사각형 그리기
t.color("blue")
t.forward(300)
t.right(90)
t.forward(300)
t.right(90)
t.forward(300)
t.right(90)
t.forward(300)
t.right(90)

# 원 그리기
t.color("green")
t.circle(150)